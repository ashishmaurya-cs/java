package com.ashish;

public class Employee {
	private int id;
	private String name;
	private double salary;
	private int v;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public int getV() {
		return v;
	}
	public void setV(int v) {
		this.v = v;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getSalary() {
		return salary;
	}
	public void setSalary(double salary) {
		this.salary = salary;
	}
	

}
