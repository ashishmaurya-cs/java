package com.ashish;

public class CompareTo {

	public static void main(String[] args) {
			String s1 ="ashish";
			String s2="ashish";
			String s3="maurya";
			System.out.println(s1.compareTo(s2));//0
			System.out.println(s1.compareTo(s3));//-1
			System.out.println(s3.compareTo(s2));//;+1
	}

}
