package com.ashish;

public class UserMapObject {
	int id;
	String name,email;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	@Override
	public String toString() {
		return "UserMapObject [id=" + id + ", name=" + name + ", email=" + email + "]";
	}
	
}
